def emit_let_chain(bindings, body):
    """Emit nested let expressions."""
    if not bindings:
        return body
    name, value = bindings[0]
    rest = emit_let_chain(bindings[1:], body)
    return [f"(let [{name} {value}]"] + [f"  {line}" for line in rest] + [")"]

