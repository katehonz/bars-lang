def emit_defn(name, params, body):
    """Emit a Bars defn with AUTO-BALANCED parens. 
    Body lines should close all inner opens. emit_defn closes the outer defn.
    If a body line ends with `...))` those are part of the body.
    AUTO-CLOSE: if body has more opens than closes, we add the needed ) at the end."""
    w.emit(f"(defn {name} {params}")
    for line in body:
        w.emit(f"  {line}")
    
    # Calculate net opens in body — these must be closed
    net = sum(line.count('(') - line.count(')') for line in body)
    if net > 0:
        # Add closing parens on a new line for clarity
        w.emit("  " + ")" * net)
    elif net < 0:
        # Body has too many closes — subtract from the auto-close
        # This shouldn't normally happen
        pass
    w.emit(")")
    w.emit("")

