# int-str: number to string
w.emit_raw("(defn int-str [n]")
w.emit_raw("  (let [d \"0123456789\"]")
w.emit_raw("    (if (< n 0)")
w.emit_raw("      (str-concat \"-\" (int-str (- 0 n)))")
w.emit_raw("      (if (< n 10)")
w.emit_raw("        (str-slice d n (+ n 1))")
w.emit_raw("        (str-concat (int-str (/ n 10)) (str-slice d (% n 10) (+ (% n 10) 1)))))))")
w.emit_raw("")

# fresh-temp, fresh-label
w.emit_raw("(defn fresh-temp [t] (str-concat \"t\" (int-str t)))")
w.emit_raw("(defn fresh-label [l prefix] (str-concat prefix (int-str l)))")
w.emit_raw("")

# put
