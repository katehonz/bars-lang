import textwrap

class BarsWriter:
    def __init__(self):
        self.lines = []
        self.indent = 0
    
    def emit(self, text):
        self.lines.append("  " * self.indent + text)
    
    def emit_raw(self, text):
        self.lines.append(text)
    
    def write(self):
        return "\n".join(self.lines) + "\n"

w = BarsWriter()

